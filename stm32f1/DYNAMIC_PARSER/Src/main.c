void blink(void)
{
	static uint32_t millis= 0;
	if (HAL_GetTick() >millis) {
		millis = HAL_GetTick()+250;
		LD2_GPIO_Port->ODR ^=LD2_Pin;
	}

}
void generate_ticket(uint16_t tickets)
{

	ASSERT_ACTIVATION;
	HAL_Delay(100);
	while(tickets-->0) {
		TICKET_GPIO_Port->ODR |= TICKET_Pin;
		HAL_Delay(50);
		TICKET_GPIO_Port->ODR &=~TICKET_Pin;
		HAL_Delay(10);
		LD2_GPIO_Port->ODR ^=LD2_Pin;
	}
	DE_ASSER_ACTIVATION;
}

